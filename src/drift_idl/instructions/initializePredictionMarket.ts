import { PROGRAM_ID } from "../programId";

export interface InitializePredictionMarketAccounts {
  admin: PublicKey;
  state: PublicKey;
  perpMarket: PublicKey;
}

export function initializePredictionMarket(
  accounts: InitializePredictionMarketAccounts,
  programId: PublicKey = PROGRAM_ID
) {
  const keys: Array<AccountMeta> = [
    { pubkey: accounts.admin, isSigner: true, isWritable: false },
    { pubkey: accounts.state, isSigner: false, isWritable: false },
    { pubkey: accounts.perpMarket, isSigner: false, isWritable: true },
  ];
  const identifier = Buffer.from([248, 70, 198, 224, 224, 105, 125, 195]);
  const data = identifier;
  const ix = new TransactionInstruction({ keys, programId, data });
  return ix;
}
